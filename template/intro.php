<h3 id="perkenalan">1. Perkenalan</h3>
<div class="pl-25">
  <h4>a. Apa itu Dapodik Synchronizer?</h4>
  <p>
    Dapodik Synchronizer adalah sebuah tool tambahan dari Actudent yang dapat melakukan penarikan data dari Dapodik ke dalam database Actudent. 
    Dapodik Synchronizer dibuat untuk memudahkan sekolah mengisi data master di Actudent yang cukup banyak. 
    Dengan cara ini, Actudent akan dapat lebih cepat digunakan oleh sekolah.
  </p>
  <h4>b. Bagaimana cara kerjanya?</h4>
  <p>
    Dapodik Synchronizer merupakan aplikasi terpisah dari Actudent,
    namun terkoneksi dengan Sync API yang akan dirilis bersamaan dengan Actudent-v2.
    Dapodik Synchronizer harus diinstal di komputer yang terinstal Dapodik 
    karena Web Service yang digunakan adalah Web Service lokal Dapodik.
  </p>
  <h4>c. Apa itu Sync API?</h4>
  <p>
    Sync API adalah sebuah sub-sistem dari Actudent
    yang bertugas mengelola data yang masuk dari luar aplikasi Actudent agar dapat masuk ke dalam database.
    Sync API juga diproyeksikan untuk pengembangan Actudent di masa depan,
    di mana Actudent bisa meningkatkan fitur-fiturnya dengan terhubung ke aplikasi di luar Actudent.
  </p>
</div>