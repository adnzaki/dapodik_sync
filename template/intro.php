<h3 id="perkenalan">1. Perkenalan</h3>
<div class="pl-25">
  <h4>a. Apa itu Dapodik Puller?</h4>
  <p>
    Dapodik Puller adalah sebuah tool tambahan dari Actudent yang dapat melakukan penarikan data dari Dapodik ke dalam database Actudent. 
    Dapodik Puller dibuat untuk memudahkan sekolah mengisi data master di Actudent yang cukup banyak. 
    Dengan cara ini, Actudent akan dapat lebih cepat digunakan oleh sekolah.
  </p>
  <h4>b. Bagaimana cara kerjanya?</h4>
  <p>
    Dapodik Puller merupakan aplikasi terpisah dari Actudent,
    namun terkoneksi dengan Sync API yang merupakan fitur teknis baru dari ActudentV2.
    Dapodik Puller <strong>harus diinstal di komputer yang terinstal Dapodik</strong> 
    karena Web Service yang digunakan adalah Web Service lokal Dapodik.
  </p>
  <h4>c. Apa itu Sync API?</h4>
  <p>
    Sync API adalah sebuah sub-sistem dari Actudent
    yang bertugas mengelola data yang masuk dari luar aplikasi Actudent agar dapat masuk ke dalam database.
    Sync API juga diproyeksikan untuk pengembangan Actudent di masa depan,
    di mana Actudent bisa meningkatkan fitur-fiturnya dengan terhubung dengan aplikasi di luar Actudent.
  </p>
</div>